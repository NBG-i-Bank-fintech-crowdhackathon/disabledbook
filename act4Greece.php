Το crowdfunding ενεργοποιεί τη θέληση του κοινού να συμβάλει στην επιτυχία στόχων, παρακινώντας το να γίνει κομμάτι οποιουδήποτε τύπου κοινωνικής ή επιχειρηματικής πρωτοβουλίας επιθυμεί να στηρίξει, ενθαρρύνοντας τη συλλογική συμμετοχή και ικανοποιώντας την επιθυμία του να συνεισφέρει ενεργά στην πράξη.



